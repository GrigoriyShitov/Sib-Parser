/*
 * Fill bytes with some garbage with specified probability (more or less).
 */
static void
junk_bytes_with_probability(uint8_t *buf, size_t size, double prob)
{
    static int junkmode;
    uint8_t *ptr;
    uint8_t *end;
    if (opt_jprob <= 0.0)
        return;
    for (ptr = buf, end = ptr + size; ptr < end; ptr++)
    {
        int byte = *ptr;
        if (junkmode++ & 1)
        {
            if ((((double)random() / RAND_MAX) < prob))
                byte = random() & 0xff;
        }
        else
        {
#define BPROB(b) ((((double)random() / RAND_MAX) < prob) ? b : 0)
            byte ^= BPROB(0x80);
            byte ^= BPROB(0x40);
            byte ^= BPROB(0x20);
            byte ^= BPROB(0x10);
            byte ^= BPROB(0x08);
            byte ^= BPROB(0x04);
            byte ^= BPROB(0x02);
            byte ^= BPROB(0x01);
        }
        if (byte != *ptr)
        {
            *ptr = byte;
        }
    }
}
#endif /* JUNKTEST */
